    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        cli.setCpf( ftfCpf.getText() );
        cli.setNome( txNome.getText() );
        cli.setNascimento( ftfData.getText() );
        cli.setTelefone( ftfTelefone.getText() );
        cli.setEmail( txEmail.getText() );
        cli.setEndereco( txEndereco.getText() );
        
        try {
            clientedbc.gravarCliente(cli);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Clientes.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Clientes.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btSalvarActionPerformed

    private void btBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBuscarActionPerformed
        String cpf;
        cliente cli = null;
        
        cpf = ftfCpf.getText();
        
        try {
            try {
                cli = clientedbc.buscarCliente(cpf);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(Clientes.class.getName()).log(Level.SEVERE, null, ex);
            }
        } catch (SQLException ex) {
                Logger.getLogger(Clientes.class.getName()).log(Level.SEVERE, null, ex);
            }
        
        txNome.setText(cli.getNome());
        ftfData.setText(cli.getNascimento());
        ftfTelefone.setText(cli.getTelefone());
        txEndereco.setText(cli.getEndereco());
        txEmail.setText(cli.getEmail());
    }//GEN-LAST:event_btBuscarActionPerformed

