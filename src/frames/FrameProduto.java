    private void btEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEditarActionPerformed
        String codigo;
        codigo = txtCodigo.getText();
        prod.setProduto((String)cbxProduto.getSelectedItem());
        prod.setQuantidade(Integer.parseInt(txtQuantidade.getText()));// conversão de inteiro para string 
        prod.setCor(txtCor.getText());
        try {
            try {
                acessoBD.editarDadosProduto(codigo, prod);
            } catch (ClassNotFoundException ex) {
             System.out.println("Erro: " + ex.toString());
            }
      }
      catch(SQLException erro){
          System.out.println("Exeção: "+ erro.toString());
      }
        
    }//GEN-LAST:event_btEditarActionPerformed

