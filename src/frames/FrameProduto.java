    private void btBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBuscarActionPerformed

       Produto prod = null;
        String codigo; 
        
     codigo = txtCodigo.getText();
      try {
          prod= acessoBD.buscarDadosProduto(codigo);
      } catch (ClassNotFoundException ex) {
          Logger.getLogger(FrameProduto.class.getName()).log(Level.SEVERE, null, ex);
      } catch (SQLException ex) {
          Logger.getLogger(FrameProduto.class.getName()).log(Level.SEVERE, null, ex);
      }
        
        txtCodigo.setText(prod.getCodigo());
        txtQuantidade.setText(String.valueOf(prod.getQuantidade()));
        cbxProduto.setSelectedItem(prod.getProduto());
        txtCor.setText(prod.getCor());
    }//GEN-LAST:event_btBuscarActionPerformed

